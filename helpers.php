/**
 * Проверяет переданную дату на соответствие формату 'ГГГГ-ММ-ДД'
 *
 * Примеры использования:
 * is_date_valid('2019-01-01'); // true
 * is_date_valid('2016-02-29'); // true
 * is_date_valid('2019-04-31'); // false
 * is_date_valid('10.10.2010'); // false
 * is_date_valid('10/10/2010'); // false
 *
 * @param string $date Дата в виде строки
 *
 * @return bool true при совпадении с форматом 'ГГГГ-ММ-ДД', иначе false
 */
function is_date_valid(string $date) : bool {
    $format_to_check = 'Y-m-d';
    $dateTimeObj = date_create_from_format($format_to_check, $date);

    return $dateTimeObj !== false && array_sum(date_get_last_errors()) === 0;
}

/**
 * Создает подготовленное выражение на основе готового SQL запроса и переданных данных
 *
 * @param $link mysqli Ресурс соединения
 * @param $sql string SQL запрос с плейсхолдерами вместо значений
 * @param array $data Данные для вставки на место плейсхолдеров
 *
 * @return mysqli_stmt Подготовленное выражение
 */
function db_get_prepare_stmt($link, $sql, $data = []) {
    $stmt = mysqli_prepare($link, $sql);

    if ($stmt === false) {
        $errorMsg = 'Не удалось инициализировать подготовленное выражение: ' . mysqli_error($link);
        die($errorMsg);
    }

    if ($data) {
        $types = '';
        $stmt_data = [];

        foreach ($data as $value) {
            $type = 's';

            if (is_int($value)) {
                $type = 'i';
            }
            else if (is_string($value)) {
                $type = 's';
            }
            else if (is_double($value)) {
                $type = 'd';
            }

            if ($type) {
                $types .= $type;
                $stmt_data[] = $value;
            }
        }

        $values = array_merge([$stmt, $types], $stmt_data);

        $func = 'mysqli_stmt_bind_param';
        $func(...$values);

        if (mysqli_errno($link) > 0) {
            $errorMsg = 'Не удалось связать подготовленное выражение с параметрами: ' . mysqli_error($link);
            die($errorMsg);
        }
    }

    return $stmt;
}

/**
 * Возвращает корректную форму множественного числа
 * Ограничения: только для целых чисел
 *
 * Пример использования:
 * $remaining_minutes = 5;
 * echo "Я поставил таймер на {$remaining_minutes} " .
 *     get_noun_plural_form(
 *         $remaining_minutes,
 *         'минута',
 *         'минуты',
 *         'минут'
 *     );
 * Результат: "Я поставил таймер на 5 минут"
 *
 * @param int $number Число, по которому вычисляем форму множественного числа
 * @param string $one Форма единственного числа: яблоко, час, минута
 * @param string $two Форма множественного числа для 2, 3, 4: яблока, часа, минуты
 * @param string $many Форма множественного числа для остальных чисел
 *
 * @return string Рассчитанная форма множественнго числа
 */
function get_noun_plural_form (int $number, string $one, string $two, string $many): string
{
    $number = (int) $number;
    $mod10 = $number % 10;
    $mod100 = $number % 100;

    switch (true) {
        case ($mod100 >= 11 && $mod100 <= 20):
            return $many;

        case ($mod10 > 5):
            return $many;

        case ($mod10 === 1):
            return $one;

        case ($mod10 >= 2 && $mod10 <= 4):
            return $two;

        default:
            return $many;
    }
}

/**
 * Подключает шаблон, передает туда данные и возвращает итоговый HTML контент
 * @param string $name Путь к файлу шаблона относительно папки templates
 * @param array $data Ассоциативный массив с данными для шаблона
 * @return string Итоговый HTML
 */
function include_template($name, array $data = []) {
    $name = 'templates/' . $name;
    $result = '';

    if (!is_readable($name)) {
        return $result;
    }

    ob_start();
    extract($data);
    require $name;

    $result = ob_get_clean();

    return $result;
}

function prices($item) {
    $price_form = ceil($item);
    return $price_form = number_format($price_form, 0, "", " ").' ₽';
}

function get_end_time($end_date) {
    $end_time_sec = strtotime($end_date) - strtotime("now");
    $end_time = date("H:i", $end_time_sec);
    return $end_time;
}

function get_enough_time($end_date) {
    $not_enough_time = false;
    if(strtotime($end_date) - strtotime("now") < 3601){
        $not_enough_time = true;
    }
    return $not_enough_time;
}

function db_fetch_data($type, $link, $sql, $data = []){
    $result = [];
    $stmt = db_get_prepare_stmt($link, $sql, $data);
    mysqli_stmt_execute($stmt);
    $res = mysqli_stmt_get_result($stmt);
    if ($type) {
        if ($res) {
            $result = mysqli_fetch_assoc($res);
        }
        return $result;
    } else {
        if ($res) {
            $result = mysqli_fetch_all($res, MYSQLI_ASSOC);
            return $result;
        } else {
            $error = mysqli_error($link);
            print("Ошибка MySQL: " . $error);
        }
    }
}

function get_categories($link) {
    $sql = "SELECT * FROM categories;";
    $categories = db_fetch_data(false, $link, $sql);
    return $categories;
}

function get_items($link) {
    $sql = "SELECT l.id AS id, l.name AS name, image, price, UNIX_TIMESTAMP(l.end_date) AS end_date, c.name AS category
            FROM lots l
            JOIN categories c ON l.category_id = c.id
            ORDER BY l.id DESC;";
    $lots = db_fetch_data(false, $link, $sql);
    return $lots;
}

function get_lot($link, $lot_id) {
    $sql = "SELECT l.id AS id, l.name AS name, image, price, UNIX_TIMESTAMP(l.end_date) AS end_date, 
       c.name AS category, description
            FROM lots l
            JOIN categories c ON l.category_id = c.id
            WHERE l.id = $lot_id";
    $lot = db_fetch_data(true, $link, $sql);
    return $lot;
}
